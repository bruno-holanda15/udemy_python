'''
Operadores aritméticos
+, -. /, //, **, %, ()
'''

print(2 * 3)
print('Vetel é ', 4 * 'campeão')

print(2 + 3)
print('Oloco ' + 'quinho')

#número inteiro sem virgula do quociente //
print(13 // 5)

# resto da divisão %
print(13 % 5)
print(10 % 5)

print(2 ** 3)

# altera a precedência dos operadores ()
print((2 + 3) / 3)
print(2 + 3 / 3)

